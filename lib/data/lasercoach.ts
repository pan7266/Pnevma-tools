export function createDefaultCorrection(
  laserMachineId: string,
  materialId: string,
  operationType: LaserOperationType,
  lensFocalLengthMm: number,
): MachineMaterialCorrection {
  const now = new Date().toISOString();
  return {
    id: `corr-${laserMachineId}-${materialId}-${operationType}-${String(lensFocalLengthMm).replace(".", "_")}`,
    laserMachineId,
    materialId,
    operationType,
    lensFocalLengthMm,
    speedMultiplier: 1,
    minPowerBiasPercent: 0,
    maxPowerBiasPercent: 0,
    passBias: 0,
    focusBiasMm: 0,
    lineIntervalMultiplier: null,
    confidenceScore: 0,
    samplesCount: 0,
    lastFeedbackAt: null,
    createdAt: now,
    updatedAt: now,
  };
}

